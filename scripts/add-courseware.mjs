#!/usr/bin/env node

import { constants as fsConstants } from 'node:fs';
import {
  access,
  mkdir,
  readFile,
  rename,
  rm,
  stat,
  writeFile
} from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.dirname(SCRIPT_DIR);
const COURSEWARE_DIR = path.join(REPO_ROOT, 'courseware');
const DATA_FILE = path.join(REPO_ROOT, 'js', 'courseware-data.js');
const DATA_HEADER = '// AUTO-GENERATED BY scripts/add-courseware.mjs\n';
const DEFAULTS = {
  description: '课堂互动练习',
  icon: 'screen',
  tone: 'purple'
};

const HELP_TEXT = `课件上传脚本

用法：
  node scripts/add-courseware.mjs --file "课件.html" --description "一句话说明"

选项：
  --file <path>          待上传的完整单文件 HTML（必选）
  --description <text>  课件说明，默认“课堂互动练习”
  --date <YY.MM.DD>      覆盖本地当天日期，例如 26.07.16
  --title <text>         覆盖课件标题及文件基础名称
  --id <text>            覆盖课件 ID；重复时直接报错
  --icon <text>          图标名称，默认 screen
  --tone <text>          色调名称，默认 purple
  --dry-run              只显示计划，不写入文件
  --help                 显示本说明

示例：
  node scripts/add-courseware.mjs --file "课件.html" --date "26.07.16" --description "一般现在时练习"
  node scripts/add-courseware.mjs --file "课件.html" --date "26.07.16" --dry-run
`;

function fail(message) {
  throw new Error(message);
}

function parseArgs(argv) {
  const valueOptions = new Set([
    '--file',
    '--description',
    '--date',
    '--title',
    '--id',
    '--icon',
    '--tone'
  ]);
  const flagOptions = new Set(['--dry-run', '--help']);
  const options = {};

  for (let index = 0; index < argv.length; index += 1) {
    const argument = argv[index];
    if (flagOptions.has(argument)) {
      const key = argument.slice(2).replace('-', '');
      options[key] = true;
      continue;
    }
    if (!valueOptions.has(argument)) {
      fail(`未知参数：${argument}`);
    }
    const value = argv[index + 1];
    if (value === undefined || value.startsWith('--')) {
      fail(`参数 ${argument} 缺少值`);
    }
    const key = argument.slice(2);
    if (Object.hasOwn(options, key)) {
      fail(`参数 ${argument} 不得重复`);
    }
    options[key] = value;
    index += 1;
  }

  return options;
}

function requireText(value, label) {
  if (typeof value !== 'string' || value.trim() === '') {
    fail(`${label} 不能为空`);
  }
  return value.trim();
}

function formatLocalDate(date = new Date()) {
  const year = String(date.getFullYear() % 100).padStart(2, '0');
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  return `${year}.${month}.${day}`;
}

function parseDate(value) {
  const match = /^(\d{2})\.(\d{2})\.(\d{2})$/.exec(value);
  if (!match) {
    fail(`日期格式无效：${value}（应为 YY.MM.DD）`);
  }

  const fullYear = 2000 + Number(match[1]);
  const month = Number(match[2]);
  const day = Number(match[3]);
  const date = new Date(fullYear, month - 1, day);
  if (
    date.getFullYear() !== fullYear ||
    date.getMonth() !== month - 1 ||
    date.getDate() !== day
  ) {
    fail(`日期不是有效日历日期：${value}`);
  }

  return {
    display: value,
    iso: `${fullYear}-${match[2]}-${match[3]}`
  };
}

function validateTitle(title) {
  const normalized = requireText(title, '标题');
  if (
    normalized === '.' ||
    normalized === '..' ||
    /[<>:"/\\|?*\u0000-\u001f]/.test(normalized) ||
    /[. ]$/.test(normalized)
  ) {
    fail(`标题不能用作安全文件名：${normalized}`);
  }
  return normalized;
}

function toDataFile(items) {
  return `${DATA_HEADER}window.COURSEWARE_ITEMS = ${JSON.stringify(items, null, 2)};\n`;
}

function parseDataFile(source) {
  const match = /^\s*\/\/ AUTO-GENERATED BY scripts\/add-courseware\.mjs\s+window\.COURSEWARE_ITEMS\s*=\s*([\s\S]*);\s*$/.exec(source);
  if (!match) {
    fail('课件清单格式无效：无法读取 window.COURSEWARE_ITEMS JSON 数组');
  }

  let items;
  try {
    items = JSON.parse(match[1]);
  } catch (error) {
    fail(`课件清单 JSON 解析失败：${error.message}`);
  }
  if (!Array.isArray(items)) {
    fail('课件清单格式无效：window.COURSEWARE_ITEMS 必须是数组');
  }
  return items;
}

async function pathExists(filePath) {
  try {
    await access(filePath, fsConstants.F_OK);
    return true;
  } catch (error) {
    if (error.code === 'ENOENT') return false;
    throw error;
  }
}

async function validateSource(fileArgument) {
  const sourcePath = path.resolve(process.cwd(), fileArgument);
  let sourceStat;
  try {
    sourceStat = await stat(sourcePath);
  } catch (error) {
    if (error.code === 'ENOENT') fail(`源文件不存在：${sourcePath}`);
    fail(`无法检查源文件：${error.message}`);
  }
  if (!sourceStat.isFile()) fail(`源路径不是普通文件：${sourcePath}`);
  if (!/\.html?$/i.test(path.extname(sourcePath))) {
    fail('源文件扩展名必须是 .html 或 .htm');
  }

  const content = await readFile(sourcePath);
  let html;
  try {
    html = new TextDecoder('utf-8', { fatal: true }).decode(content);
  } catch {
    fail(`源文件不是有效 UTF-8：${sourcePath}`);
  }

  const requiredParts = [
    ['<!DOCTYPE html', /<!doctype\s+html\b/i],
    ['<html', /<html\b/i],
    ['<head', /<head\b/i],
    ['<body', /<body\b/i]
  ];
  const missing = requiredParts
    .filter(([, pattern]) => !pattern.test(html))
    .map(([label]) => label);
  if (missing.length > 0) {
    fail(`HTML 校验失败，缺少：${missing.join('、')}`);
  }

  return { sourcePath, content };
}

function printPlan(plan, dryRun) {
  console.log(dryRun ? '课件上传预演（未写入任何文件）' : '课件上传完成');
  console.log(`源文件：${plan.sourcePath}`);
  console.log(`标题：${plan.item.title}`);
  console.log(`ID：${plan.item.id}`);
  console.log(`目标路径：${plan.item.path}`);
  console.log(`描述：${plan.item.description}`);
  console.log(`图标：${plan.item.icon}`);
  console.log(`色调：${plan.item.tone}`);
  console.log(`自动重名编号：${plan.sequence > 1 ? `是（-${plan.sequence}）` : '否'}`);
  console.log('涉及文件：');
  console.log(`- ${plan.item.path}`);
  console.log('- js/courseware-data.js');
}

async function preparePlan(options) {
  if (!options.file) fail('缺少必选参数：--file <path>');

  const source = await validateSource(options.file);
  const date = parseDate(options.date ? requireText(options.date, '日期') : formatLocalDate());
  const baseTitle = validateTitle(options.title || date.display);
  const description = options.description === undefined
    ? DEFAULTS.description
    : requireText(options.description, '描述');
  const icon = options.icon === undefined ? DEFAULTS.icon : requireText(options.icon, '图标');
  const tone = options.tone === undefined ? DEFAULTS.tone : requireText(options.tone, '色调');
  const customId = options.id === undefined ? null : requireText(options.id, 'ID');

  let dataSource;
  try {
    dataSource = await readFile(DATA_FILE, 'utf8');
  } catch (error) {
    fail(`无法读取课件清单 ${path.relative(REPO_ROOT, DATA_FILE)}：${error.message}`);
  }
  const currentItems = parseDataFile(dataSource);
  if (customId && currentItems.some(item => item.id === customId)) {
    fail(`自定义 ID 已存在：${customId}`);
  }

  let sequence = 1;
  let item;
  let targetPath;
  while (true) {
    const suffix = sequence === 1 ? '' : `-${sequence}`;
    const title = `${baseTitle}${suffix}`;
    const relativePath = `courseware/${title}.html`;
    const id = customId || `courseware-${date.iso}${suffix}`;
    targetPath = path.join(COURSEWARE_DIR, `${title}.html`);
    item = { id, title, description, icon, tone, path: relativePath };

    const conflicts =
      await pathExists(targetPath) ||
      currentItems.some(existing => (
        existing.title === title ||
        existing.path === relativePath ||
        existing.id === id
      ));
    if (!conflicts) break;
    sequence += 1;
  }

  return {
    ...source,
    item,
    sequence,
    targetPath,
    dataSource,
    nextDataSource: toDataFile([item, ...currentItems])
  };
}

async function commitPlan(plan) {
  await mkdir(COURSEWARE_DIR, { recursive: true });
  const token = `${process.pid}-${Date.now()}-${Math.random().toString(16).slice(2)}`;
  const htmlTemp = path.join(COURSEWARE_DIR, `.${path.basename(plan.targetPath)}.${token}.tmp`);
  const dataTemp = path.join(path.dirname(DATA_FILE), `.courseware-data.${token}.tmp`);
  let htmlCommitted = false;

  try {
    await writeFile(htmlTemp, plan.content, { flag: 'wx' });
    await writeFile(dataTemp, plan.nextDataSource, { encoding: 'utf8', flag: 'wx' });

    if (await pathExists(plan.targetPath)) {
      fail(`目标文件在写入期间已出现，已停止以避免覆盖：${plan.targetPath}`);
    }
    const latestDataSource = await readFile(DATA_FILE, 'utf8');
    if (latestDataSource !== plan.dataSource) {
      fail('课件清单在写入期间发生变化，已停止以避免覆盖');
    }

    await rename(htmlTemp, plan.targetPath);
    htmlCommitted = true;
    await rename(dataTemp, DATA_FILE);
  } catch (error) {
    if (htmlCommitted) {
      await rm(plan.targetPath, { force: true }).catch(() => {});
    }
    await rm(htmlTemp, { force: true }).catch(() => {});
    await rm(dataTemp, { force: true }).catch(() => {});
    throw new Error(`写入失败，已清理临时内容：${error.message}`);
  }
}

async function main() {
  const options = parseArgs(process.argv.slice(2));
  if (options.help) {
    console.log(HELP_TEXT);
    return;
  }

  const plan = await preparePlan(options);
  if (options.dryrun) {
    printPlan(plan, true);
    return;
  }

  await commitPlan(plan);
  printPlan(plan, false);
}

main().catch(error => {
  console.error(`错误：${error.message}`);
  process.exitCode = 1;
});
